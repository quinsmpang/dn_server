taskconf.GetReward = function(taskid, signid)
	local money = 0
	local tool = nil
	local reward = taskconf.reward_money[taskid]
	if reward then
		if type(reward) == "table" and signid then
			if reward[signid] then
				money = reward[signid]
			end
		else
			money = reward
		end
	end
	if taskconf.reward_tool[taskid] then
		tool = taskconf.reward_tool[taskid]
	end
	return money, tool
end

